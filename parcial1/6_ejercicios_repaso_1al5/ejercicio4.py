 #Solicitar 2 numeros al usuario
# y realizar todas las operaciones
# basicas de una calculadora (+,-,*,/)
# y mostrar por pantalla el resultado

numero=int(input("Ingresa un numero: "))
numero2=int(input("ingresa un numero nuevamente: "))
suma=numero+numero2
resta=numero-numero2
multi=numero*numero2
div=numero/numero2
print(f"El resultado de la suma: {suma}")
print(f"El resultado de la resta: {resta}")
print(f"El resultado de la multiplicacion: {multi}")
print(f"El resultado de la division: {div}")